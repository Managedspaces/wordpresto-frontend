import type { Locale } from '@/i18n/locales';

// Every visible string on the /waitlist multi-step form, across all 6
// locales. Option `value`s are the literal strings stored in the database
// (waitlist_applications) and sent in the Resend notification email — they
// stay in English in every locale so the data stays analysable in one
// language regardless of which locale a lead applied from. Only the visible
// `label` is translated.
//
// This dictionary feeds both the server-rendered template (via
// WaitlistPage.astro) and the client-side step/validation script, which
// reads it from a serialised <script type="application/json"> tag — see
// WaitlistPage.astro for how the two are wired together.

export interface Option {
  value: string;
  label: string;
}

