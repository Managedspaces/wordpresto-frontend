// Reusable across locales: pricing-band values are identical in shape per
// currency symbol, only the label text differs.
function bands(symbol: string, t: {
  under: string; band1: string; band2: string; plus: string; needToSee: string; agency: string;
}): Option[] {
  return [
    { value: 'under_25', label: `${t.under} ${symbol}25${t.band1}` },
    { value: '25_75', label: `${symbol}25 ${t.band2} ${symbol}75${t.band1}` },
    { value: '75_150', label: `${symbol}75 ${t.band2} ${symbol}150${t.band1}` },
    { value: '150_300', label: `${symbol}150 ${t.band2} ${symbol}300${t.band1}` },
    { value: '300_plus', label: `${symbol}300${t.plus}${t.band1}` },
    { value: 'need_to_see', label: t.needToSee },
    { value: 'agency_custom', label: t.agency },
  ];
}

